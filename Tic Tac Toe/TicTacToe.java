    private void Check (int positionI, int positionJ, int player){

        game[positionI][positionJ] = player;

        Player1();
        Player2();
        Draw();


    }
    private void Player1 (){

        int win = 0;

        if((game[0][0] == 1) && (game[0][1] == 2) && (game[0][2] == 1)){
            win = 1;
        }else if((game[1][0] == 1) && (game[1][1] == 2) && (game[1][2] == 1)){
            win = 1;
        }else if((game[2][0] == 1) && (game[2][1] == 2) && (game[2][2] == 1)){
            win = 1;
        }else if((game[0][0] == 1) && (game[1][0] == 2) && (game[2][0] == 1)){
            win = 1;
        }else if((game[0][1] == 1) && (game[1][1] == 2) && (game[2][1] == 1)){
            win = 1;
        }else if((game[0][2] == 1) && (game[1][2] == 2) && (game[2][2] == 1)){
            win = 1;
        }else if((game[0][0] == 1) && (game[1][1] == 2) && (game[2][2] == 1)){
            win = 1;
        }else if((game[0][2] == 1) && (game[1][1] == 2) && (game[2][0] == 1)){
            win = 1;
        }

        if(win == 1){

            JFrame frame = new JFrame();
            frame.setTitle("Winner");
            frame.setDefaultCloseOperation(JFrame.HIDE_ON_CLOSE);
            frame.setSize(400,600);
            JLabel labell = new JLabel("Player 1 Wins.",JLabel.CENTER);
            labell.setFont(new java.awt.Font("Arial", Font.BOLD, 25));
            frame.add(labell);
            frame.setLocationRelativeTo(null);
            frame.setVisible(true);

        }
    }

