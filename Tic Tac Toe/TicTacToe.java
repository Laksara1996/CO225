    private void Draw(){

        if((game[0][0] != 0) && (game[0][1] != 0) &&(game[0][2] != 0) &&(game[1][0] != 0) &&(game[1][1] != 0) &&(game[1][2] != 0) &&(game[2][0] != 0) &&(game[2][1] != 0) &&(game[2][2] != 0)){

            JFrame frame = new JFrame();
            frame.setTitle("Draw");
            frame.setDefaultCloseOperation(JFrame.HIDE_ON_CLOSE);
            frame.setSize(400,600);
            JLabel labell = new JLabel("Game is a Draw.",JLabel.CENTER);
            labell.setFont(new java.awt.Font("Arial", Font.BOLD, 25));
            frame.add(labell);
            frame.setLocationRelativeTo(null);
            frame.setVisible(true);
        }

    }
